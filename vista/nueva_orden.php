                  if (service.description != null) {
                    jQuery('#descriptionContainer').removeClass('hidden');
                    jQuery('#description').html(service.description);
                  } else {
                    jQuery('#descriptionContainer').addClass('hidden');
                    jQuery('#description').html('');
                  }
              
                  if (service.name.includes("Refill:")) {
                    start = service.name.indexOf("Refill:");
                    end = service.name.indexOf("]", start);
                    content = service.name.substring(start, end);
                    jQuery('#guaranteed').text(content.split(":")[1]);
                  } else {
                    jQuery('#guaranteed').text("N/A");
                  }
              
